#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	i;
	int	j;

	i = 0;
	while (argc == 2 && argv[1][i] != '\0')
	{
		if (argv[1][i] >= 65 && argv[1][i] <= 90)
		{
			j = 0;
			while (argv[1][i] - 64 - j++ > 0)
				write(1, &argv[1][i], 1);
		}
		else if (argv[1][i] >= 97 && argv[1][i] <= 122)
		{
			j = 0;
			while (argv[1][i] - 96 - j++ > 0)
				write(1, &argv[1][i], 1);
		}
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
